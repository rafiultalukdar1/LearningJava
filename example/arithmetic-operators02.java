public class Arithmetic {

    public static void main(String[] args) {

        int a = 12;
        int b = 5;
        int result;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        result = a + b;
        System.out.println("a + b = " + result);

        result = a - b;
        System.out.println("a - b = " + result);

        result = a * b;
        System.out.println("a * b = " + result);

        result = a / b;
        System.out.println("a / b = " + result);

        result = a % b;
        System.out.println("a % b = " + result);
    }
}